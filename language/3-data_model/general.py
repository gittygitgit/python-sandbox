# are variables statically typed in python?
# no.  

# What is type-checking?
# A process used by a program to ensure only valid information is passed between program elements
# With type-checking, a program will only work for specifically defined types 

# Why type-checking?
# Reduce bugs

# When is type-checking performed?
# Varies by language.

# How do interfaces relate to type-checking?
# type-checking involves verifying an invocation of a program element complies with its interface definition

# What is static typing?
# Process where a programming language performs type-checking during compile-time

# Why static typing?
# Catches errors early in the development cycle

# What is dynamic typing?
# Process where type-checking is performed during runtime
# In dynamic typing, values have types, but variables do not.



