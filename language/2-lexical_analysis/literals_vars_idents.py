# What is a variable?
# A name for a value

# Why variables?
# Allows you to reference a value by a name

# What is a literal?
# Something which mean exactly what they say.

# What is the difference between literals and variables?
# A literal does not reference anything, whereas a variable references something else.
# A literal's value can be determined directly from it's name, whereas a variable's cannot

# What is an identifier?
# The sequence of characters used to form a variable's name


